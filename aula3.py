"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
String são textos que estão dentro de Aspas

"""

# Aspas Simples
print('Jose Henrique')
print('Jose "Henrique"')

# Aspas Duplas
print("Jose Henrique")
print("Jose 'Henrique'")

# Escape
print("Jose \"Henrique\"")

# r (expressões regulares)
print(r"Jose \"Henrique\"") 